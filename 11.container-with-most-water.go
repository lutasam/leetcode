// @lc code=start
func maxArea(height []int) int {
	if len(height) == 0 {
		return 0
	}
	mostWater, left, right := 0, 0, len(height)-1
	for left < right {
		mostWater = max(mostWater, min(height[left], height[right])*(right-left))
		if height[left] < height[right] {
			left++
		} else {
			right--
		}
	}
	return mostWater
}

func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}

